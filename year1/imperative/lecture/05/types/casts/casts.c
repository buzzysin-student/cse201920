#include <stdint.h>

int main(void)
{
  /*
   * The header `stdint.h` introduces a bunch of useful 
   * types for integers:
   */

  int8_t signed8bit;
  int16_t signed16bit;
  int32_t signed32bit;
  int64_t signed64bit;

  uint8_t unsigned8bit;
  uint16_t unsigned16bit;
  uint32_t unsigned32bit;
  uint64_t unsigned64bit;

  return 0;
}